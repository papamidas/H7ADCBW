/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  size_t output_buflen;
  /* USER CODE END 1 */

  /* Enable I-Cache---------------------------------------------------------*/
  SCB_EnableICache();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART3_UART_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */
  if(HAL_OK != HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adc_buf, 2*ADC_HALFBUFLEN)) {
	Error_Handler();
  }
  adcBufDMAState = DMA_INPROGRESS;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	while(adcBufDMAState != DMA_HALFCOMPLETE)
	 {}
	base64_encode((uint8_t *)adc_buf, 2*ADC_HALFBUFLEN, tx_buf, &output_buflen);
	tx_buf[output_buflen] = '\r';
	tx_buf[output_buflen+1] = '\n';

	if(HAL_OK != HAL_UART_Transmit_DMA(&huart3, (uint8_t *)tx_buf, output_buflen+2))
	{
		Error_Handler();
    }
	txBufDMAState = DMA_INPROGRESS;
	while(txBufDMAState != DMA_COMPLETE)
	  {} //wait till all data sent
	adcBufDMAState = DMA_INPROGRESS;
  }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  /* USER CODE END 3 */
}

